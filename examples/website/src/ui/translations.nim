var spokenLang: cstring
buildJs:
  ~spokenLang = localStorage["happyx_spoken_language"]
if spokenLang.len != 0:
  languageSettings.set($spokenLang)


var languages* = @[
  ("English", "en"),
  ("Русский", "ru")
]

proc langTitles*: seq[string] =
  result = @[]
  for i in languages:
    result.add(i[0])
proc langCodes*: seq[string] =
  result = @[]
  for i in languages:
    result.add(i[1])
